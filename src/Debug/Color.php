  private static function start_color(int $color): string {
    return "\033[38;5;" . $color . "m";
  }

  private static function reset_color(): string {
    return "\033[0m";
  }

  public static function sprintf(int $color, string $format, ...$args): string {
    return (
      self::start_color($color) .
      \sprintf($format, ...$args) .
      self::reset_color()
    );
  }

  public static function str_repeat(int $color, string $str, int $times): string {
    return (
      self::start_color($color) .
      \str_repeat($str, $times) .
      self::reset_color()
    );
  }

  public static function printf(int $color, string $format, ...$args): void {
    echo self::sprintf($color, $format, ...$args);
  }
